void connect_wifi() {
  Serial.print("Connecting to WiFi");
  WiFi.begin(wifi_ssid, wifi_password);
  while (WiFi.status() != WL_CONNECTED) {
    Serial.print(".");
    delay(200);
  }
  Serial.println("\nConnected.");
}


void setup() {
  Serial.begin(115200);
  delay(1000);

  pinMode(LED, OUTPUT);
  digitalWrite(LED, LOW);

  dht.begin();

  Serial.println("=== Etape 1 - INTERNNEEEEET (MQTT) ===");

  connect_wifi();

  mqtt_client.setServer(mqtt_server, mqtt_port);
  client.setCACert(ca_cert);
  

  float humidity = dht.readHumidity();
  float temperature = dht.readTemperature();

  bool ok = mqtt_client.connect(client_id, mqtt_user, mqtt_pass);

  if (ok && !isnan(humidity) && !isnan(temperature)) {
    String topic_temp = String(client_id) + "/temp";
    String topic_hum  = String(client_id) + "/relhum";

    char payload_temp[16];
    char payload_hum[16];
    dtostrf(temperature, 0, 2, payload_temp);
    dtostrf(humidity, 0, 2, payload_hum);

    mqtt_client.publish(topic_temp.c_str(), payload_temp, true);
    mqtt_client.publish(topic_hum.c_str(), payload_hum, true);

    mqtt_client.loop();
    delay(1000);
    mqtt_client.loop();

    mqtt_client.disconnect();


  }

  Serial.println("Passage en deep sleep...");
  Serial.println();

  esp_sleep_enable_timer_wakeup((uint64_t)SLEEP_TIME * 1000000ULL);
  esp_deep_sleep_start();
}

